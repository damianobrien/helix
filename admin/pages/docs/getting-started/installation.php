<h1>Installing Helix</h1>
<p>
	Installing Helix is very simple.
</p>
<ul>
	<li><a target="_blank" href="https://sourceforge.net/projects/helix/files/Helix%20PHP%20Framework/helix.zip/download">Download Helix</a> from the project site</li>
	<li>Unzip the file in the web server root directory, e.g. <code>/var/www/htdocs/</code></li>
	<li>Go to <code>http://www.sample.com/helix</code>, but of course use your domain name</li>
</ul>
<p>
	That URL will take you to the Helix administration pages, which you can use to create new applications and configure the framework for your server.
</p>