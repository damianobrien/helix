<h1>Creating a New Application</h1>
<p>
	After installing Helix, go the Helix administration pages at <code>http://www.sample.com/helix</code> and click on <em>Create a new application</em>. 
</p>
<p>
	Enter a name for your application and click the <code>Create Application</code> button.  Your application folder should appear next to the <code>helix</code> folder in your web server root directory. 
</p>